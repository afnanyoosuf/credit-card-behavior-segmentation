import numpy as np
import pandas as  pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.metrics import silhouette_score,davies_bouldin_score

def evaluate_clustering(scaled_data, labels, name):
    sil = silhouette_score(scaled_data, labels)
    dbi = davies_bouldin_score(scaled_data, labels)
    print(f"--- {name} ---")
    print(f"Silhouette Score     : {sil:.4f}  (higher is better, max=1)")
    print(f"Davies-Bouldin Index : {dbi:.4f}  (lower is better, min=0)")
    print()
    return sil, dbi

def compare_algorithms(scaled_data, kmeans_labels, hierarchical_labels):
    print("=" * 45)
    print("       CLUSTERING EVALUATION RESULTS")
    print("=" * 45)
    km_sil, km_dbi = evaluate_clustering(scaled_data, kmeans_labels, "KMeans")
    hc_sil, hc_dbi = evaluate_clustering(scaled_data, hierarchical_labels, "Hierarchical")
    results = pd.DataFrame({
        "Algorithm"        : ["KMeans", "Hierarchical"],
        "Silhouette Score" : [km_sil, hc_sil],
        "Davies-Bouldin"   : [km_dbi, hc_dbi]
    })
    print(results.to_string(index=False))
    print()
    if km_sil > hc_sil:
        print("KMeans performs BETTER based on Silhouette Score")
    else:
        print("Hierarchical performs BETTER based on Silhouette Score")
    return results

def plot_evaluation(results):
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))
    axes[0].bar(results["Algorithm"], results["Silhouette Score"],color=["steelblue", "orange"], edgecolor="black")
    axes[0].set_title("Silhouette Score\n(Higher is Better)")
    axes[0].set_ylabel("Score")
    for i, v in enumerate(results["Silhouette Score"]):
        axes[0].text(i, v + 0.001, f"{v:.4f}", ha="center", fontweight="bold")
    axes[1].bar(results["Algorithm"], results["Davies-Bouldin"],color=["steelblue", "orange"], edgecolor="black")
    axes[1].set_title("Davies-Bouldin Index\n(Lower is Better)")
    axes[1].set_ylabel("Score")
    for i, v in enumerate(results["Davies-Bouldin"]):
        axes[1].text(i, v + 0.001, f"{v:.4f}", ha="center", fontweight="bold")
    plt.suptitle("Clustering Evaluation Comparison", fontsize=13)
    plt.tight_layout()
    plt.savefig("evaluation_comparison.png")
    plt.show()

def plot_silhouette_per_cluster(scaled_data, labels, name):
    from sklearn.metrics import silhouette_samples
    sil_vals = silhouette_samples(scaled_data, labels)
    df = pd.DataFrame({"silhouette": sil_vals, "cluster": labels})
    plt.figure(figsize=(8, 5))
    colors = plt.cm.tab10.colors
    y_lower = 10
    for i in sorted(df["cluster"].unique()):
        cluster_vals = df[df["cluster"] == i]["silhouette"].sort_values()
        size = len(cluster_vals)
        y_upper = y_lower + size
        plt.barh(range(y_lower, y_upper), cluster_vals,height=1.0, color=colors[i % len(colors)], alpha=0.7)
        plt.text(-0.05, (y_lower + y_upper) / 2, f"C{i}")
        y_lower = y_upper + 10
    avg = df["silhouette"].mean()
    plt.axvline(x=avg, color="red", linestyle="--", label=f"Avg = {avg:.3f}")
    plt.title(f"Silhouette Plot - {name}")
    plt.xlabel("Silhouette Coefficient")
    plt.ylabel("Cluster")
    plt.legend()
    plt.tight_layout()
    filename = name.lower().replace(" ", "_")
    plt.savefig(f"silhouette_{filename}.png")
    plt.show()

